-- 헤비 유저가 소유한 장소


-- -- WHERE 절로 필터링
SELECT ID,
    NAME,
    HOST_ID
FROM PLACES
WHERE HOST_ID IN
(
    SELECT HOST_ID
    FROM PLACES
    GROUP BY HOST_ID
    HAVING COUNT(*) > 1
)
ORDER BY ID


-- -- INNER JOIN 으로 필터링
SELECT P1.ID, 
    P1.NAME, 
    P1.HOST_ID
FROM PLACES P1
INNER JOIN 
(
    SELECT HOST_ID
    FROM PLACES
    GROUP BY HOST_ID
    HAVING COUNT(*) >= 2
) A ON P1.HOST_ID=A.HOST_ID
ORDER BY P1.ID